package org.sdrc.cpis.models;

public class CasteModel {
	private Integer cciId;
	private String category;
	private String boys;
	private String girls;
	private String thirdGender;
	public Integer getCciId() {
		return cciId;
	}
	public void setCciId(Integer cciId) {
		this.cciId = cciId;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	public String getBoys() {
		return boys;
	}
	public void setBoys(String boys) {
		this.boys = boys;
	}
	public String getGirls() {
		return girls;
	}
	public void setGirls(String girls) {
		this.girls = girls;
	}
	public String getThirdGender() {
		return thirdGender;
	}
	public void setThirdGender(String thirdGender) {
		this.thirdGender = thirdGender;
	}
	
	
}
