package org.sdrc.cpis.models;

public class GenderModel {
	private int boysCount;
	private int girlsCount;
	private int thirdGenderCount;
	private int total;
	public int getBoysCount() {
		return boysCount;
	}
	public void setBoysCount(int boysCount) {
		this.boysCount = boysCount;
	}
	public int getGirlsCount() {
		return girlsCount;
	}
	public void setGirlsCount(int girlsCount) {
		this.girlsCount = girlsCount;
	}
	public int getThirdGenderCount() {
		return thirdGenderCount;
	}
	public void setThirdGenderCount(int thirdGenderCount) {
		this.thirdGenderCount = thirdGenderCount;
	}
	public int getTotal() {
		return total;
	}
	public void setTotal(int total) {
		this.total = total;
	}
	
	
}
